// 업종 슬러그 검증
export function isWorkTypeSlug(slug: string): slug is WorkTypeSlug {
    if (WORK_TYPE_SLUGS.includes(slug as WorkTypeSlug)) return true;
    const cleanSlug = slug.endsWith('알바') ? slug.replace('알바', '') : slug;
    return WORK_TYPE_SLUGS.includes(cleanSlug as WorkTypeSlug);
}

export function getNormalizedWorkTypeSlug(slug: string): WorkTypeSlug | undefined {
    if (WORK_TYPE_SLUGS.includes(slug as WorkTypeSlug)) return slug as WorkTypeSlug;
    const cleanSlug = slug.endsWith('알바') ? slug.replace('알바', '') : slug;
    if (WORK_TYPE_SLUGS.includes(cleanSlug as WorkTypeSlug)) {
        return cleanSlug as WorkTypeSlug;
    }
    return undefined;
}

